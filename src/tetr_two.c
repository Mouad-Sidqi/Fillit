#include "fillit.h"

int		tetr_two(int *tab)
{
	if (tab[0] == 1 && tab[1] == 4 && tab[2] == 1)
		return (4);
	if (tab[0] == 1 && tab[1] == 1 && tab[2] == 3)
		return (10);
	if (tab[0] == 1 && tab[1] == 1 && tab[2] == 2)
		return (13);
	if (tab[0] == 1 && tab[1] == 1 && tab[2] == 4)
		return (17);
	if (tab[0] == 4 && tab[1] == 1 && tab[2] == 1)
		return (19);
	return (0);
}

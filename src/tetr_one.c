#include "fillit.h"

int		tetr_one(int *tab)
{
	if (tab[0] == 1 && tab[1] == 3 && tab[2] == 1)
		return (1);
	if (tab[0] == 4 && tab[1] == 1 && tab[2] == 4)
		return (7);
	if (tab[0] == 4 && tab[1] == 1 && tab[2] == 3)
		return (9);
	if (tab[0] == 1 && tab[1] == 4 && tab[2] == 4)
		return (12);
	if (tab[0] == 4 && tab[1] == 4 && tab[2] == 1)
		return (14);
	if (tab[3] % 4 != 3 && tab[0] == 1 && tab[1] == 3 && tab[2] == 4)
		return (16);
	return (0);
}
